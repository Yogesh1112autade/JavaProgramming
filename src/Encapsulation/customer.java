package Encapsulation;

public class customer {

	private int accNo;
	private String cusname;
	
	public void setAccno(int an) {
		this.accNo=an;
	}
	
	public void setCusname(String cn) {
		this.cusname=cn;
	}
	
	public int getAccno() {
		return accNo;
	}
	
	public String getCusname() {
		return cusname;
	}
	
}
